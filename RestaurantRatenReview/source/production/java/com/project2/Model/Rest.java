package com.project2.Model;

public class Rest {
	private int restId;
	private String restName;
	private String location;
	private float rate_avg; 
	private int count;
	/**
	 * @return the restId
	 */
	public int getRestId() {
		return restId;
	}
	/**
	 * @param restId the restId to set
	 */
	public void setRestId(int restId) {
		this.restId = restId;
	}
	/**
	 * @return the restName
	 */
	public String getRestName() {
		return restName;
	}
	/**
	 * @param restName the restName to set
	 */
	public void setRestName(String restName) {
		this.restName = restName;
	}
	/**
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}
	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}
	/**
	 * @return the rate_avg
	 */
	public float getRate_avg() {
		return rate_avg;
	}
	/**
	 * @param rate_avg the rate_avg to set
	 */
	public void setRate_avg(float rate_avg) {
		this.rate_avg = rate_avg;
	}
	/**
	 * @return the count
	 */
	public int getCount() {
		return count;
	}
	/**
	 * @param count the count to set
	 */
	public void setCount(int count) {
		this.count = count;
	}
	
	
}
